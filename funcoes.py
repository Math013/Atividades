"""Função 3 notas"""

def media():
  soma = 0
  for x in range(0,3):
    nota = float(input(f"Informe a {x+1}ª nota: "))
    soma += nota
  media = soma / 3

  if media >= 7:
    print(f"Sua média é {media:.1f}")
    print("Aprovado!")
  elif 5 <= media < 7:
    print(f"Sua média é {media:.1f}")
    print("Recuperação!")
  else:
    print(f"Sua média é {media:.1f}")
    print("Reprovado!")

media()

