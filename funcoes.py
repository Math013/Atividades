def medias():
  notas = []
  for x in range(0,10):
    n = float(input(f"Informe a {x+1}ª nota: "))
    notas.append(n)
  
  media = sum(notas) / len(notas)

  if media >= 7:
    print(f"Sua média é {media:.1f}")
